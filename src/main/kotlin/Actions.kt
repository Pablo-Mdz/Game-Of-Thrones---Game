/**
 * Displays the heroes and enemies actual HP.
 */
fun actualHpOffAll(heroes: List<Heroes>, enemies: List<Enemy>) {
    println("$bgGreen$white-------------HEROES HP---------------$reset")
    heroes.forEach {
        println("- The actual Hp from ${it.name} is: ${it.hp}")
    }
    println("$bgpink$white-------------ENEMIES HP--------------$reset")
    enemies.forEach {
        println("- The actual Hp from ${it.name} is: ${it.hp}")
    }
    println("$bgWhite-------------------------------------$reset")
    println()
}
